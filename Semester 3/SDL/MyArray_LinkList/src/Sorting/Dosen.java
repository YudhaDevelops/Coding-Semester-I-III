package Sorting;
public class Dosen {
    private String nama;
    private int nip;

    public Dosen() {
    }

    public Dosen(String nama, int nip) {
        this.nama = nama;
        this.nip = nip;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getNip() {
        return nip;
    }

    public void setNip(int nip) {
        this.nip = nip;
    }
    
    
}
