    private void jButtonNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextActionPerformed
        // TODO add your handling code here:
        if (!data.isEmpty()) {
            data.remove();
            tampilData();
            jLabelOutput.setVisible(false);
        } else{
            jLabelOutput.setVisible(true);
            jLabelOutput.setText("Antrian sedang kosong");
            tfNama.setText("");
        }
        
    }//GEN-LAST:event_jButtonNextActionPerformed

    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        if (!tfNama.getText().isEmpty() && data.search(tfNama.getText()) == 0) {
            data.add(tfNama.getText());
            tampilData();
            tfNama.setText("");
            jLabelOutput.setVisible(false);
        } else if (data.search(tfNama.getText()) != 0) {
            jLabelOutput.setVisible(true);
            jLabelOutput.setText("Data sudah ada");
            tfNama.setText("");
        } else{
            jLabelOutput.setVisible(true);
            jLabelOutput.setText("Text Field tidak boleh kosong");
            tfNama.setText("");
        }

    }//GEN-LAST:event_jButtonAddActionPerformed

