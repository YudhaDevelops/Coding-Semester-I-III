    public void tampilData() {
        ArrayList<Object> dataCustomer = data.ambilData();
        String[] kolom = {"No", "Nama"};
        Object[][] objData = new Object[dataCustomer.size()][2]; // 6 merupakan jumlah kolom
        int i = 1;
        for (int j = 0; j < dataCustomer.size(); j++) {
            Customer customer = (Customer) dataCustomer.get(j);
            objData[j] = new String[]{String.valueOf(i), customer.getNama()};
            i++;
        }
        tabelModel = new DefaultTableModel(objData, kolom) {
            @Override
            public boolean isCellEditable(int rowIndex, int colIndex) {
                return false;
            }
        };
//        jTableCustomer.repaint();
        jTableCustomer.setModel(tabelModel);
        columnModel = jTableCustomer.getColumnModel();
        columnModel.getColumn(0).setPreferredWidth(10);
        columnModel.getColumn(1).setPreferredWidth(350);
    }

