    private void jButtonCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCariActionPerformed
        // TODO add your handling code here:
        if (!data.isEmpty() && data.search(tfNama.getText()) != 0) {
            jLabelOutput.setVisible(true);
            jLabelOutput.setText(tfNama.getText() + " ada pada antrian ke "
                    + data.search(tfNama.getText()));
        } else {
            jLabelOutput.setVisible(true);
            jLabelOutput.setText("Data tidak ditemukan");
        }
        tfNama.setText("");
    }//GEN-LAST:event_jButtonCariActionPerformed

